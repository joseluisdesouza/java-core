package com.java.core.Zintroducaologica;

public class class07Arrays03 {
    public static void main(String[] args) {
        String[] nomes = new String[3];
        nomes[0] = "Vegeta";
        nomes[1] = "Kuririn";
        nomes[2] = "Goku";
        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);
        }
    }
}
