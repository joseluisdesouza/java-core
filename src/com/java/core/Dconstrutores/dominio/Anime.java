package com.java.core.Dconstrutores.dominio;

public class Anime {
    private String nome;
    private String tipo;
    private int episodios;
    private String genero;
    private String estudio;
    //colocamos mais um atributo e agora? não podemos alterar o construtor ja criado
    //para não quebrar outras partes do codigo, e então criamos outro construtor e
    //adicionamos o atributo inserido no objeto Anime estudio

    public Anime(String nome, String tipo, int episodios, String genero) {
        this();
        this.nome = nome;
        this.tipo = tipo;
        this.episodios = episodios;
        this.genero = genero;
    }

    //sobrecarga de construtor, esse construtor ira delegar responsabilidade para o primeiro construtor
    public Anime(String nome, String tipo, int episodios, String genero, String estudio) {
        this(nome, tipo, episodios, genero);
        this.estudio = estudio;
        //this(); só funciona dentro de construtor e é obrigatorio ser primeiro
    }

    public Anime() {
        System.out.println("Sem argumento");
    }

    public void imprime() {
        System.out.println(this.nome);
        System.out.println(this.tipo);
        System.out.println(this.episodios);
        System.out.println(this.estudio);
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getEpisodios() {
        return episodios;
    }

    public void setEpisodios(int episodios) {
        this.episodios = episodios;
    }
}
