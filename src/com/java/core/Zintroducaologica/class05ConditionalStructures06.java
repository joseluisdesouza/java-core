package com.java.core.Zintroducaologica;

public class class05ConditionalStructures06 {
    public static void main(String[] args) {
        byte dia = 5;
        switch (dia) {
            case 1:
                break;
            case 7:
                System.out.println("Final de semana");
                break;
            case 3:
            case 4:
            case 5:
            case 6:
                System.out.println("Dial util");
                break;
            default:
                System.out.println("Opção invalida");
                break;
        }
    }
}
